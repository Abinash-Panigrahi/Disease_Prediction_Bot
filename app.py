"""
app.py
------
The main Frontend application file for Clino Health Innovation.
This file handles the Streamlit user interface, injects custom CSS 
for styling, captures user symptom input, and displays the final 
AI-generated medical report.
"""

import streamlit as st
import json                                        
from api import configure_gemini, analyze_symptoms  # ← import from api.py

# ─── Page Config ───────────────────────────────────────────────────────────────
st.set_page_config(
    page_title="Clino Health Innovation",
    page_icon="🏥",
    layout="centered"
)

# ─── Configure Gemini ──────────────────────────────────────────────────────────
# app.py calls configure_gemini() and handles any error with st.error()
try:
    configure_gemini()
except ValueError as e:
    st.error(f"❌ {str(e)}")   # ← st.error lives here in app.py ✅
    st.stop()

# ─── Custom CSS ────────────────────────────────────────────────────────────────
# Read the CSS file and inject it
with open("style.css", "r") as css_file:
    st.markdown(f"<style>{css_file.read()}</style>", unsafe_allow_html=True)

# ─── Header ────────────────────────────────────────────────────────────────────
st.markdown("""
<div class="main-header">
    <h1>🏥 Clino Health Innovation</h1>
    <p>AI-Powered Symptom Checker &amp; Disease Predictor</p>
</div>
""", unsafe_allow_html=True)

# ─── Input Section ─────────────────────────────────────────────────────────────
st.markdown('<p class="section-label">📝 Describe Your Symptoms</p>', unsafe_allow_html=True)
st.markdown('<p class="section-sub">Be as detailed as possible — duration, severity, and any other details help improve accuracy.</p>', unsafe_allow_html=True)

symptoms = st.text_area(
    label="Describe your symptoms",
    label_visibility="collapsed",
    placeholder="e.g. I have had a high fever, sore throat, body aches, and fatigue for the past 2 days...",
    height=170,
    key="symptom_input"
)

# ─── Predict Button ─────────────────────────────────────────────────────────────
col1, col2, col3 = st.columns([0.8, 2.4, 0.8])
with col2:
    predict_btn = st.button("🔍  Predict My Condition", use_container_width=True)

# ─── On Button Click ────────────────────────────────────────────────────────────
if predict_btn:
    if not symptoms.strip():
        st.warning("⚠️ Please describe your symptoms before clicking Predict.")
    else:
        with st.spinner("🤖 Analyzing your symptoms with Gemini AI..."):
            try:
                # ── api.py does all the AI work, app.py just shows results ──
                result = analyze_symptoms(symptoms)

                st.markdown("<hr>", unsafe_allow_html=True)
                st.markdown('<p class="report-title">🩺 Your Health Report</p>', unsafe_allow_html=True)

                # Predicted Disease
                st.markdown(f"""
                <div class="result-card disease-card">
                    <p class="card-heading">🦠 &nbsp;Predicted Condition</p>
                    <div class="card-body">{result['predicted_disease']}</div>
                </div>
                """, unsafe_allow_html=True)

                # Home Remedies
                remedies_li = "".join([f"<li>{r}</li>" for r in result['home_remedies']])
                st.markdown(f"""
                <div class="result-card remedies-card">
                    <p class="card-heading">🌿 &nbsp;Home Remedies</p>
                    <div class="card-body"><ul>{remedies_li}</ul></div>
                </div>
                """, unsafe_allow_html=True)

                # Precautions
                precautions_li = "".join([f"<li>{p}</li>" for p in result['precautions']])
                st.markdown(f"""
                <div class="result-card precautions-card">
                    <p class="card-heading">⚠️ &nbsp;Precautions</p>
                    <div class="card-body"><ul>{precautions_li}</ul></div>
                </div>
                """, unsafe_allow_html=True)

                # Disclaimer
                st.markdown("""
                <div class="disclaimer">
                    ⚕️ <strong>Medical Disclaimer:</strong> This prediction is generated by AI and is intended
                    for informational purposes only. It is <strong>not a substitute</strong> for professional
                    medical advice, diagnosis, or treatment. Please consult a qualified healthcare provider
                    if your symptoms persist or worsen.
                </div>
                """, unsafe_allow_html=True)

            except json.JSONDecodeError:
                st.error("⚠️ Could not read the AI response. Please try again.")  # ← st.error in app.py ✅
            except Exception as e:
                st.error(f"❌ Something went wrong: {str(e)}")                    # ← st.error in app.py ✅

# ─── Footer ────────────────────────────────────────────────────────────────────
st.markdown("""
<div class="footer">
    © 2025 Clino Health Innovation &nbsp;|&nbsp; Powered by Gemini AI &nbsp;|&nbsp; For demonstration purposes only
</div>
""", unsafe_allow_html=True)